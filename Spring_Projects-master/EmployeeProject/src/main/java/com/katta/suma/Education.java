package com.katta.suma;

public class Education {
	private float tenth;
	private float inter;
	private float btech;
	private String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getTenth() {
		return tenth;
	}
	public void setTenth(float tenth) {
		this.tenth = tenth;
	}
	public float getInter() {
		return inter;
	}
	public void setInter(float inter) {
		this.inter = inter;
	}
	public float getBtech() {
		return btech;
	}
	public void setBtech(float btech) {
		this.btech = btech;
	}
	

}
